function generarPropuesta() {
  let frase = "";
  for (let i = 0; i < propuestas.length; i++) {
    const tipoDePropuesta = propuestas[i]; // objeto
    const parteDelaPropuesta = Object.keys(tipoDePropuesta); // [ intenciones,acciones,etc ]

    for (let j = 0; j < parteDelaPropuesta.length; j++) {
      let posiblesParteDelTexto = tipoDePropuesta[parteDelaPropuesta[j]]; // ["queremos", "pensamos","planeamos",etc]
      let randomIndex = Math.floor(
        Math.random() * posiblesParteDelTexto.length
      );
      let texto = posiblesParteDelTexto[randomIndex];
      frase += texto;
    }
    frase += "\n";
  }

  console.log(frase);
}

